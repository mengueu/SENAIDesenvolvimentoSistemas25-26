#4: Soma de Elementos em Lista Aninhada
'''
Dada a lista numeros = [[1, 2, 3], [4, 5, 6], [7, 8, 9]] , calcule e imprima:
1. A soma dos elementos da primeira sublista ( [1, 2, 3] ).
2. Imprima o último elemento da última sublista.
'''

numeros = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print("A soma dos elementos da primeira sublista é:",numeros[0][0],"+",numeros[0][1],"+",numeros[0][2],"=",numeros[0][0]+numeros[0][1]+numeros[0][2])
print("O último elemento da última sublista é:",numeros[2][2])

