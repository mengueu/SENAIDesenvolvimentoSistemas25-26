var numero1 = prompt("Digite o primeiro número para realizar a adição:")
var numero2 = prompt("Digite o segundo número para realizar a adição:")

let adicao = Number(numero1) + Number(numero2)
alert("O resultado da adição é: " + adicao)
console.log("Adição: " + numero1 + " + " + numero2 + " = " + adicao)

